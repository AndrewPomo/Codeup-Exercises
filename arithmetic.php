<?php

function add($a, $b)
{
	return $a + $b;
}

function subtract($a, $b)
{
    return $a - $b;
}

function multiply($a, $b)
{
    return $a * $b;
}

function divide($a, $b)
{
    return $a / $b;
}

echo add(50, 5);
echo PHP_EOL;
echo subtract(50, 5);
echo PHP_EOL;
echo multiply(50, 5);
echo PHP_EOL;
echo divide(50, 5);
echo PHP_EOL;
